// Procedural
// Funções que manipulam dados
processamento(valor1,valor2,valor3)

// OO -> dados e dentro deles você tem funções
// estrutura que agrupa tanto atributos como comportamentos
objeto = {
    valor1,valor2,valor3,
    processamento(){
        //...
    }
}
// e agora temos os dados que tem a função (comportamento)
// ou seja, o foco passou a ser o objeto, ao invés da fn (no caso de procedural)
objeto.processamento()

